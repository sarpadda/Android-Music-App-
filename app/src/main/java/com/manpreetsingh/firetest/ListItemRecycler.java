public class ListItemRecycler {

    private String head;
    private String desc;

    public ListItemRecycler(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
